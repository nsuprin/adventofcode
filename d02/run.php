  public static function parse2($aInput) {
      foreach ($aInput as $item) {
          list($sMovement, $iDelta) = explode(' ', $item);
          switch ($sMovement) {
              case 'forward':
                  self::$ix += $iDelta;
                  self::$iY += $iDelta * self::$iAim;
                  break;
              case 'down':
                  self::$iAim += $iDelta;
                  break;
              case 'up':
