  /**
   * @param $aInput
   * @return float|int
   */
  public static function parse($aInput) {
    foreach ($aInput as $item) {
        list($sMovement, $iDelta) = explode(' ', $item);
        switch ($sMovement) {
            case 'forward':
                self::$ix += $iDelta;
                break;
            case 'down':
                self::$iY += $iDelta;
                break;
            case 'up':
                self::$iY -= $iDelta;
                break;
        }
    }
    $iReturn = self::$iY * self::$ix;
    return $iReturn;
  }

