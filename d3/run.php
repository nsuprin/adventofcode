  public static function parse2($aInput) {
    $iTree  = 1;
    $aSlopes  = [
      [1, 1],
      [3, 1],
      [5, 1],
      [7, 1],
      [1, 2],
    ];
    foreach ($aSlopes as $aSlope) {
      list(self::$iDeltaX, self::$iDeltaY)  = $aSlope;
      self::$iX = self::$iY = 0;
      $iTree  *= self::parse($aInput);
    }
    return $iTree;
  }

  public static function parse($aInput) {
    $iTree  = $iIteration = 0;
    self::$iMaxX  = strlen($aInput[0]) - 1;
    self::$iMaxY  = sizeof($aInput);
    self::$iY += self::$iDeltaY;
    self::$iX += self::$iDeltaX;

    while (self::$iY < self::$iMaxY) {
      $iIteration++;
      $sContent  = substr($aInput[self::$iY], self::$iX, 1);
      //var_dump([self::$iX, self::$iY, $sContent]);
      if ('#' == $sContent) {
        $iTree++;
      }
      self::$iY += self::$iDeltaY;
      self::$iX += self::$iDeltaX;
      if (self::$iX > self::$iMaxX) {
        self::$iX = self::$iX - self::$iMaxX - 1;
      }
    }
    return $iTree;
  }
